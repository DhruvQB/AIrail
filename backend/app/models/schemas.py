# Pydantic request/response schemas
# models/schemas.py
# FastAPI-level Pydantic schemas — request validation and response serialization.
# These are the public contract between the React frontend and the FastAPI backend.
# Nothing inside agents/ should ever import from this file.

from pydantic import BaseModel, Field, field_validator
from typing import Optional, Literal
from datetime import date


# ---------------------------------------------------------------------------
# Shared / base
# ---------------------------------------------------------------------------

class BaseResponse(BaseModel):
    """
    Common envelope wrapping every API response.
    Gives the frontend a consistent shape to check before reading payload.
    """
    success: bool
    message: str                          # human-readable status e.g. "OK" or error detail


# ---------------------------------------------------------------------------
# /chat
# ---------------------------------------------------------------------------

class ChatRequest(BaseModel):
    session_id: str = Field(
        ...,
        min_length=8,
        max_length=64,
        description="UUID identifying the user's conversation session.",
        examples=["a1b2c3d4-e5f6-7890-abcd-ef1234567890"],
    )
    message: str = Field(
        ...,
        min_length=1,
        max_length=1000,
        description="The user's natural language query.",
        examples=["What is the PNR status of 1234567890?"],
    )

    @field_validator("message")
    @classmethod
    def message_not_blank(cls, v: str) -> str:
        if not v.strip():
            raise ValueError("message must not be blank or whitespace only.")
        return v.strip()


class ChatResponse(BaseResponse):
    session_id: str
    intent: Optional[str] = Field(
        default=None,
        description="The intent the supervisor classified this query as.",
    )
    response: str = Field(
        ...,
        description="Natural language answer generated by the responsible agent.",
    )


