class PNRRequest(BaseModel):
    pnr_number: str = Field(
        ...,
        min_length=10,
        max_length=10,
        pattern=r"^\d{10}$",
        description="10-digit PNR number.",
        examples=["1234567890"],
    )
    session_id: Optional[str] = None


class PassengerStatus(BaseModel):
    """
    Booking status of a single passenger on the PNR.
    Nested inside PNRResponse.
    """
    passenger_index: int = Field(
        ...,
        ge=1,
        description="Passenger number (1-based).",
    )
    booking_status: str = Field(
        ...,
        description="Status at time of booking. E.g. 'WL/42'.",
        examples=["WL/42", "CNF", "RAC/12"],
    )
    current_status: str = Field(
        ...,
        description="Current live status. E.g. 'CNF/S5/32'.",
        examples=["CNF/S5/32", "RAC/12", "WL/3"],
    )
    coach: Optional[str] = Field(
        default=None,
        description="Assigned coach. Populated only if confirmed.",
        examples=["S5", "B2"],
    )
    berth: Optional[str] = Field(
        default=None,
        description="Assigned berth number and type. Populated only if confirmed.",
        examples=["32/LB", "14/UB"],
    )


class PNRResponse(BaseResponse):
    pnr_number: str
    train_number: str
    train_name: str
    journey_date: str
    source_station: str
    destination_station: str
    class_type: str
    chart_prepared: bool = Field(
        ...,
        description="Whether the reservation chart has been prepared for this train.",
    )
    passengers: list[PassengerStatus]
    natural_language_summary: str = Field(
        ...,
        examples=[
            "Your PNR 1234567890 for Rajdhani Express on 15 Jan is confirmed. "
            "Passenger 1 is in Coach S5, Berth 32 (Lower)."
        ],
    )


