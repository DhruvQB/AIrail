class TrainStatusRequest(BaseModel):
    train_number: str = Field(
        ...,
        min_length=4,
        max_length=6,
        pattern=r"^\d{4,6}$",
        description="Indian Railways train number (4-6 digits).",
        examples=["12951", "22222"],
    )
    journey_date: Optional[str] = Field(
        default=None,
        description="Optional journey date in YYYY-MM-DD format. Defaults to today in IST.",
    )
    session_id: Optional[str] = Field(
        default=None,
        description="Optional session ID to associate with conversation memory.",
    )


class NextStationDetails(BaseModel):
    stationCode: str
    expectedArrival: Optional[str] = None
    expectedDeparture: Optional[str] = None
    distanceToNextStationKm: Optional[float] = None
    delayArrivalMinutes: Optional[int] = None


class CurrentLocation(BaseModel):
    latitude: Optional[float] = None
    longitude: Optional[float] = None
    stationCode: Optional[str] = None
    status: Optional[str] = None
    distanceFromOriginKm: Optional[float] = None
    nextStation: Optional[NextStationDetails] = None


class RouteStop(BaseModel):
    sequence: int
    stationCode: str
    scheduledArrival: Optional[str] = None
    scheduledDeparture: Optional[str] = None
    actualArrival: Optional[str] = None
    actualDeparture: Optional[str] = None
    delayArrivalMinutes: Optional[int] = None
    delayDepartureMinutes: Optional[int] = None
    expectedDelayArrivalMinutes: Optional[int] = None
    expectedDelayDepartureMinutes: Optional[int] = None
    platform: Optional[str] = None


class TrainStatusResponse(BaseResponse):
    trainNumber: str
    trainName: str = "Unknown"
    journeyDate: Optional[str] = None
    lastUpdatedAt: Optional[str] = None
    currentLocation: Optional[CurrentLocation] = None
    route: list[RouteStop] = Field(default_factory=list)
    natural_language_summary: str = Field(
        ...,
        description="Agent-generated human-readable summary of the train's current status.",
        examples=["Rajdhani Express is currently at Varanasi and running 15 minutes late."],
    )


# ---------------------------------------------------------------------------
# /search-trains
# ---------------------------------------------------------------------------

class SearchTrainsRequest(BaseModel):
    source: str = Field(
        ...,
        min_length=2,
        max_length=5,
        pattern=r"^[A-Z]{2,5}$",
        description="Source station code in uppercase.",
        examples=["ST", "NDLS"],
    )
    destination: str = Field(
        ...,
        min_length=2,
        max_length=5,
        pattern=r"^[A-Z]{2,5}$",
        description="Destination station code in uppercase.",
        examples=["BCT", "HWH"],
    )
    session_id: Optional[str] = None

    @field_validator("destination")
    @classmethod
    def source_destination_different(cls, v: str, info) -> str:
        source = info.data.get("source")
        if source and v == source:
            raise ValueError("source and destination cannot be the same station.")
        return v


class TrainSummary(BaseModel):
    """
    Represents one train in the search results list.
    Nested inside SearchTrainsResponse.
    """
    train_number: str
    train_name: Optional[str] = "Unknown"
    departure_time: str = Field(..., examples=["06:20"])
    arrival_time: str = Field(..., examples=["13:45"])
    duration: str = Field(..., examples=["7h 25m"])
    days_of_operation: list[str] = Field(
        ...,
        description="Days this train runs. E.g. ['Mon', 'Wed', 'Fri']",
    )
    available_classes: list[str] = Field(
        ...,
        description="Classes available on this train.",
        examples=[["SL", "3A", "2A", "1A"]],
    )
    distance_km: Optional[float] = None


class SearchTrainsResponse(BaseResponse):
    source: str
    destination: str
    journey_date: Optional[str] = None
    total_trains_found: int = Field(..., ge=0)
    trains: list[TrainSummary]
    natural_language_summary: str = Field(
        ...,
        description="Agent-generated summary of available trains.",
        examples=["Found 6 trains from Surat to Mumbai on 15 Jan. Earliest is 06:20 Shatabdi."],
    )


