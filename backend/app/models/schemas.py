class AvailabilityRequest(BaseModel):
    train_number: str = Field(
        ...,
        min_length=4,
        max_length=6,
        pattern=r"^\d{4,6}$",
        examples=["12951"],
    )
    source: str = Field(
        ...,
        min_length=2,
        max_length=5,
        pattern=r"^[A-Z]{2,5}$",
        examples=["ST"],
    )
    destination: str = Field(
        ...,
        min_length=2,
        max_length=5,
        pattern=r"^[A-Z]{2,5}$",
        examples=["BCT"],
    )
    journey_date: date = Field(..., examples=["2025-01-15"])
    class_type: Literal["SL", "3A", "2A", "1A", "CC", "EC", "2S"] = Field(
        ...,
        description="Coach class to check availability for.",
    )
    session_id: Optional[str] = None

    @field_validator("journey_date")
    @classmethod
    def journey_date_not_in_past(cls, v: date) -> date:
        from datetime import date as date_cls
        if v < date_cls.today():
            raise ValueError("journey_date cannot be in the past.")
        return v


class QuotaAvailability(BaseModel):
    """
    Availability under a single quota type.
    Nested inside AvailabilityResponse.
    """
    quota: Literal["GN", "TQ", "LD", "HP", "SS"] = Field(
        ...,
        description="GN=General, TQ=Tatkal, LD=Ladies, HP=Handicapped, SS=Senior Citizen",
    )
    available_seats: int = Field(..., ge=0)
    status: Literal["AVAILABLE", "RAC", "WL", "REGRET"] = Field(
        ...,
        description="AVAILABLE=seats open, RAC=reservation against cancellation, "
                    "WL=waitlist, REGRET=no booking possible.",
    )
    waitlist_number: Optional[int] = Field(
        default=None,
        description="Current waitlist position. Populated only when status is WL.",
        ge=1,
    )
    fare: Optional[float] = Field(
        default=None,
        description="Fare in INR for this class and quota.",
        ge=0,
    )


class AvailabilityResponse(BaseResponse):
    train_number: str
    train_name: str
    source: str
    destination: str
    journey_date: str
    class_type: str
    quotas: list[QuotaAvailability] = Field(
        ...,
        description="Availability broken down by quota type.",
    )
    natural_language_summary: str = Field(
        ...,
        examples=["12951 Rajdhani has 42 seats available in 3A under General quota on 15 Jan."],
    )


